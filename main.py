number = int(input("введите целое число:"))  # запрашиваем пользователя ввести целое число
if number % 2 == 0: # прописываем оператор if/else
    print("число является четным.")  # выводим текст, если число кратно 2
else:  # если условие ложное
    print("число является нечетным.")  # выводим текст, если число не кратно 2
